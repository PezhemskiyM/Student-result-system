    private void jbtnResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnResultActionPerformed
        
        //Сделать проверку некорректных значений
        int[] score = new int[listScores.length];
        int i = 0;
        float sumScore = 0;
        int countfilled = 0;
        for (JTextField c:listScores){
            int scorei;
            if (c.getText().isEmpty()){
                scorei = 0;
            }
            else{
                scorei = Integer.parseInt(c.getText());
            }
            if (scorei !=0){
                score[i] = scorei;
                sumScore += score[i];
                countfilled++;
            }
            i++;
        }

        float avgScore = sumScore / countfilled;
        jtxtTotalScore.setText(Float.toString(avgScore));
        
        String ranking;
        if (avgScore == 5.0) 
        {
            ranking = "Отлично";
        }
        else if (avgScore > 4.0)
        {
            ranking = "Хорошо";
        }
        else if (avgScore > 3.0)
        {
            ranking = "Удовлетворительно";
        }
        else if (avgScore > 2.0)
        {
            ranking = "Неудовлетворительно";
        }
        else
        {
            ranking = "Плохо";
        }
        jtxtRanking.setText(ranking);
        
        Calendar ToDay = Calendar.getInstance();
        ToDay.getTime();
        SimpleDateFormat Tdate = new SimpleDateFormat("dd-MMM-yyyy");
        jtxtDate.setText(Tdate.format(ToDay.getTime()));
        
        
        
    }//GEN-LAST:event_jbtnResultActionPerformed

